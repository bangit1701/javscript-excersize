// for, do ...while , while...
// doi voi for..in(dung cho doi tuong) va forEach(dung cho mang)
//vi du in day so tu 0 den 5
console.log("Vong lap FOR");
var x = 10;
for (x; x < 5; x++) {
  console.log(`x = ${x}`);
}
console.log("Vong lap do...while");
x = 10;
do {
  console.log(`x = ${x}`);
  x++;
} while (x < 5);
console.log("VOng lap while...");
x = 10; //khoi tao x
while (x < 5) {
  console.log(`x = ${x}`);
  x++;
}
